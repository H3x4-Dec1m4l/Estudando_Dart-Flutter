// Operadores de Atribuição (Binário/infix)
main() {

double a = 2; //adicionando um valor a variável
a = a + 3; //Atribuindo a soma ao valor a variável anterior
a += 3; // outra maneira de fazer a mesma ação de cima
a -= 3; //atribuindo subtração a variável
a *= 3; //atribuindo a multiplicação
a /= 5; //atribuindo divisão a variável
a %= 2; //atribuindo resto de divisão a variável

//Operadores Relacionais (Binário/infix) -> o resultado sempre é BOOL

print(3 > 2); //maior que
print(3 >= 3); //maior ou igual que
print(3 < 4); //menor que
print(3 <= 3); //menor ou igual que
print(3 != 3); //diferente de
print(3 == 3); //igual a
print(3 == '3'); /*igual a (obs: aqui daria falso pois está comparando
um número inteiro com uma string, e uma string não é igual a um 
número inteiro)

*/
/*
então aqui faz comparação com operdores, por isso 
operadores relacionais.
*/
}
